package com.intelligentsia.dowsers.entity.model;

import java.io.Serializable;
import java.util.Locale;

/**
 * LocalizedDescription.
 * 
 * 
 */
public class LocalizedDescription implements Serializable {

	/**
	 * serialVersionUID:long
	 */
	private static final long serialVersionUID = -7681581389688921173L;

	private Locale locale;

	private String description;

	public LocalizedDescription() {
		super();
	}

	public LocalizedDescription(final Locale locale, final String description) {
		super();
		this.locale = locale;
		this.description = description;
	}

	public Locale getLocale() {
		return locale;
	}

	public void setLocale(final Locale locale) {
		this.locale = locale;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(final String description) {
		this.description = description;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = (prime * result) + ((description == null) ? 0 : description.hashCode());
		result = (prime * result) + ((locale == null) ? 0 : locale.hashCode());
		return result;
	}

	@Override
	public boolean equals(final Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		final LocalizedDescription other = (LocalizedDescription) obj;
		if (description == null) {
			if (other.description != null) {
				return false;
			}
		} else if (!description.equals(other.description)) {
			return false;
		}
		if (locale == null) {
			if (other.locale != null) {
				return false;
			}
		} else if (!locale.equals(other.locale)) {
			return false;
		}
		return true;
	}

}
